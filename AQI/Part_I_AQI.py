# Cau 5: AQI có những mức nào? Những mức nào là chỉ số đáng báo động?
# answers:
#           1 / 0-50   :         Good
#           2 / 51-100 :         Moderate
#           3 / 101-150:         Unhealthy for Sensitive Groups
#           4 / 151-200:         Unhealthy
#           5 / 201-300:         Very Unhealthy
#           6 / 300+   :         Hazardous
#      
#           Mức 3 - 6 là đáng báo động
