# Cau 1: Chỉ số chất lượng về không khí (AQI) là gì?
# answers:
# Chỉ số chất lượng không khí (AQI) (Air Quality Index) là một chỉ số báo cáo chất lượng không khí hàng ngày. 
# Nó cho bạn biết không khí quanh bạn là sạch hay ô nhiễm và những ảnh hưởng liên quan tới sức khỏe của bạn.
# Chỉ số AQI tập trung vào sự ảnh hưởng sức khỏe bạn có thể gặp trong vòng vài giờ hoặc vài ngày sau khi hít thở không khí ô nhiễm.
# 
